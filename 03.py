# Criar dicionario com espaço vazio e adicionar duas chaves e dois valores.

dicionario = {}

# pede ao usuário para digitar uma chave e um valor e adiciona ao dicionário
chave1 = input("Digite uma chave: ")
valor1 = input("Digite um valor: ")
dicionario[chave1] = valor1

# pede ao usuário para digitar outra chave e outro valor e adiciona ao dicionário
chave2 = input("Digite outra chave: ")
valor2 = input("Digite outro valor: ")
dicionario[chave2] = valor2

print("O dicionário resultante é:", dicionario)